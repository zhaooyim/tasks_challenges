//// ** Variables ** // //

/// let
// let creates a variable which can be 'reassigned' (the current value can be entirely replaced with a new value)

let string = "a"
string = "g"

/// const
// const creates a variable which can't be 'reassigned', but objects and arrays can still be 'mutated' (the values of the properties inside
// the object or array can be changed)

const string2 = "hi"
string2 = "bye"

